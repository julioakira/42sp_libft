#include "libft.h"

void	ft_bzero(void *s, size_t n)
	{
	char	*ptr;
	size_t	idx;

	idx = 0;
	ptr = s;
	while (idx < n)
	{
		ptr[idx] = '\0';
		idx++;
	}
}
